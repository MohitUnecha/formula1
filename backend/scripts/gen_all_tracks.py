def try_fastf1_circuit(year, gp_name, n_pts=120):
    """Try to load circuit from FastF1. Returns (x, y, corners, start) or None."""
    try:
        import fastf1
        fastf1.Cache.enable_cache(os.path.join(os.path.dirname(__file__), '.fastf1-cache'))
        
        session = fastf1.get_session(year, gp_name, 'R')
        session.load(telemetry=True, weather=False, messages=False)
        
        fastest = session.laps.pick_fastest()
        tel = fastest.get_telemetry()
        x_raw = tel['X'].values
        y_raw = tel['Y'].values
        
        # Get circuit info
        try:
            ci = session.get_circuit_info()
            rotation = float(ci.rotation)
            # Apply rotation
            rad = np.radians(rotation)
            cos_r, sin_r = np.cos(rad), np.sin(rad)
            x_rot = x_raw * cos_r - y_raw * sin_r
            y_rot = x_raw * sin_r + y_raw * cos_r
            
            raw_corners = []
            for _, c in ci.corners.iterrows():
                cx = float(c['X']) * cos_r - float(c['Y']) * sin_r
                cy = float(c['X']) * sin_r + float(c['Y']) * cos_r
                raw_corners.append({'number': int(c['Number']), 'x': cx, 'y': cy})
        except:
            x_rot, y_rot = x_raw, y_raw
            raw_corners = []
        
        # Normalize
        x_norm, y_norm = normalize_coords(x_rot, y_rot)
        x_smooth, y_smooth = smooth_coords(x_norm, y_norm, n_pts)
        
        # Normalize corners too
        if raw_corners:
            cx = np.array([c['x'] for c in raw_corners])
            cy = np.array([c['y'] for c in raw_corners])
            cx_n, cy_n = normalize_coords(
                np.concatenate([x_rot, cx]),
                np.concatenate([y_rot, cy])
            )
            corners = []
            for i, c in enumerate(raw_corners):
                corners.append({
                    'number': c['number'],
                    'x': int(cx_n[len(x_rot) + i]),
                    'y': int(cy_n[len(y_rot) + i]),
                })
        else:
            corners = []
        
        start = {'x': int(x_smooth[0]), 'y': int(y_smooth[0])}
        return x_smooth, y_smooth, corners, start
    except Exception as e:
        print(f"  FastF1 failed: {e}")
        return None

def generate_track(key, data, try_ff1=False, year=2024, gp_name=None):
    """Generate track SVG data for a circuit."""
    result = None
    
    if try_ff1 and gp_name:
        print(f"  Trying FastF1 for {gp_name}...")
        result = try_fastf1_circuit(year, gp_name)
    
    if result:
        x, y, corners, start = result
        path = coords_to_svg_path(x, y)
        print(f"  -> FastF1: {len(x)} pts, {len(path)} chars, {len(corners)} corners")
    else:
        # Use known coordinate data
        x = np.array(data['x'], dtype=float)
        y = np.array(data['y'], dtype=float)
        
        # Smooth the coordinates
        try:
            x_smooth, y_smooth = smooth_coords(x, y, 120)
        except:
            x_smooth, y_smooth = x, y
        
        x_norm, y_norm = normalize_coords(x_smooth, y_smooth)
        path = coords_to_svg_path(x_norm, y_norm)
        
        # Auto-generate corners at high-curvature points
        corners = detect_corners(x_norm, y_norm)
        start = {'x': int(x_norm[0]), 'y': int(y_norm[0])}
        print(f"  -> Known data: {len(x)} pts -> {len(path)} chars, {len(corners)} auto-corners")
    
    return {
        'path': path,
        'corners': corners,
        'startFinish': start,
    }

def detect_corners(x, y, threshold=0.15, max_corners=20):
    """Detect corners by finding points of high curvature."""
    n = len(x)
    if n < 5:
        return []
    
    # Compute curvature
    dx = np.gradient(x)
    dy = np.gradient(y)
    ddx = np.gradient(dx)
    ddy = np.gradient(dy)
    
    denom = (dx**2 + dy**2)**1.5
    denom[denom < 1e-10] = 1e-10
    curvature = np.abs(dx * ddy - dy * ddx) / denom
    
    # Normalize
    curvature = curvature / (curvature.max() + 1e-10)
    
    # Find peaks
    peaks = []
    for i in range(2, n-2):
        if curvature[i] > threshold and curvature[i] >= curvature[i-1] and curvature[i] >= curvature[i+1]:
            peaks.append((i, curvature[i]))
    
    # Sort by curvature and take top corners
    peaks.sort(key=lambda p: p[1], reverse=True)
    
    # Remove too-close peaks (min 5 points apart)
    filtered = []
    for idx, curv in peaks:
        if all(abs(idx - f[0]) > 5 for f in filtered):
            filtered.append((idx, curv))
        if len(filtered) >= max_corners:
            break
    
    # Sort by position along track
    filtered.sort(key=lambda p: p[0])
    
    corners = []
    for num, (idx, _) in enumerate(filtered, 1):
        corners.append({
            'number': num,
            'x': int(x[idx]),
            'y': int(y[idx]),
        })
    
    return corners

