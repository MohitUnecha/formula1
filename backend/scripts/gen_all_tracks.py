def normalize_coords(x, y, width=1000, height=700, padding=60):
    """Normalize coordinates to fit in width x height with padding."""
    x = np.array(x, dtype=float)
    y = np.array(y, dtype=float)
    
    x_range = x.max() - x.min() or 1
    y_range = y.max() - y.min() or 1
    scale = min((width - 2*padding) / x_range, (height - 2*padding) / y_range)
    
    cx = (x.min() + x.max()) / 2
    cy = (y.min() + y.max()) / 2
    
    x_norm = (x - cx) * scale + width / 2
    y_norm = (y - cy) * scale + height / 2
    
    return x_norm, y_norm

def coords_to_svg_path(x, y, close=True):
    """Convert x,y coordinate arrays to smooth SVG cubic bezier path."""
    n = len(x)
    if n < 4:
        return ""
    
    parts = [f"M {x[0]:.0f},{y[0]:.0f}"]
    
    i = 1
    while i < n - 2:
        parts.append(f"C {x[i]:.0f},{y[i]:.0f} {x[i+1]:.0f},{y[i+1]:.0f} {x[i+2]:.0f},{y[i+2]:.0f}")
        i += 3
    
    # Handle remaining points
    remaining = n - i
    if remaining == 2:
        parts.append(f"Q {x[i]:.0f},{y[i]:.0f} {x[i+1]:.0f},{y[i+1]:.0f}")
    elif remaining == 1:
        parts.append(f"L {x[i]:.0f},{y[i]:.0f}")
    
    if close:
        parts.append("Z")
    
    return " ".join(parts)

def smooth_coords(x, y, n_pts=120):
    """Smooth and downsample coordinates using cubic spline interpolation."""
    from scipy.interpolate import splprep, splev
    try:
        tck, u = splprep([x, y], s=0, per=True, k=3)
        u_new = np.linspace(0, 1, n_pts)
        x_new, y_new = splev(u_new, tck)
        return x_new, y_new
    except Exception:
        # Fallback: simple downsampling
        idx = np.linspace(0, len(x)-1, n_pts).astype(int)
        return x[idx], y[idx]

