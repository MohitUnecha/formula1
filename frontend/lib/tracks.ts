export function getTrackLayout(eventName: string, country: string): TrackLayout {
  const eventLower = eventName.toLowerCase();
  const countryLower = country.toLowerCase();
  
  // Sort by longest keyword first for specificity
  const sorted = Object.entries(TRACK_LOOKUP).sort((a, b) => b[0].length - a[0].length);
  
  // Phase 1: Match against event name ONLY (higher priority — "Miami Grand Prix" → miami, not cota)
  for (const [keyword, trackKey] of sorted) {
    if (eventLower.includes(keyword)) {
      return TRACKS[trackKey];
    }
  }
  
  // Phase 2: Match against country (fallback)
  for (const [keyword, trackKey] of sorted) {
    if (countryLower.includes(keyword)) {
      return TRACKS[trackKey];
    }
  }
  
  return TRACKS['default'];
}

