/**
 * Real F1 Circuit Track Layouts
 * Normalized coordinates (0-1000 range) for SVG rendering.
 * Each track: outline path, corners, DRS zones, pit lane, sector boundaries.
 */

export interface TrackCorner {
  x: number;
  y: number;
  number: number;
  name?: string;
}

export interface DRSZone {
  start: { x: number; y: number };
  end: { x: number; y: number };
}

export interface TrackLayout {
  name: string;
  country: string;
  length_km: number;
  laps: number;
  path: string; // SVG path for the track outline
  corners: TrackCorner[];
  drsZones: DRSZone[];
  startFinish: { x: number; y: number };
  pitEntry: { x: number; y: number };
  pitExit: { x: number; y: number };
  sectorBoundaries: { x: number; y: number }[];
  width: number;
  height: number;
}

// Helper to compute position along the track path
export function getPointAlongPath(
  pathElement: SVGPathElement | null,
  fraction: number
): { x: number; y: number } {
  if (!pathElement) return { x: 500, y: 500 };
  const length = pathElement.getTotalLength();
  const point = pathElement.getPointAtLength(fraction * length);
  return { x: point.x, y: point.y };
}

